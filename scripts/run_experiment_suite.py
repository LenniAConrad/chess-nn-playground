def _safe_name(value: str) -> str:
    return "".join(ch if ch.isalnum() or ch in "._-" else "_" for ch in value).strip("_") or "experiment"


def _load_suite(path: str | Path | None, configs: list[str]) -> dict[str, Any]:
    if path:
        suite = load_yaml(path)
    else:
        suite = {"name": "ad_hoc_experiment_suite", "configs": configs}
    if configs:
        suite["configs"] = configs
    if not suite.get("configs"):
        raise SystemExit("No configs provided. Use --suite or pass config paths.")
    return suite


def _suite_seeds(suite: dict[str, Any]) -> list[int | None]:
    seeds = suite.get("seeds")
    if seeds is None:
        return [None]
    if not isinstance(seeds, list) or not seeds:
        raise SystemExit("suite.seeds must be a non-empty list when provided")
    return [int(seed) for seed in seeds]


def _config_for_seed(config: dict[str, Any], seed: int | None) -> dict[str, Any]:
    if seed is None:
        return config
    result = copy.deepcopy(config)
    result["seed"] = int(seed)
    run_cfg = result.setdefault("run", {})
    base_name = str(run_cfg.get("name", "run"))
    suffix = f"_seed{seed}"
    if not base_name.endswith(suffix):
        run_cfg["name"] = f"{base_name}{suffix}"
    notes = str(result.get("notes", "") or "")
    seed_note = f"Seed {seed} run generated by experiment suite for repeated-seed reliability."
    result["notes"] = f"{notes} {seed_note}".strip()
    return result


def _find_existing_run(results_dir: Path, cfg_hash: str) -> Path | None:
    for metadata_path in sorted(results_dir.glob("*/run_metadata.json")):
        try:
            data = json.loads(metadata_path.read_text(encoding="utf-8"))
        except Exception:
            continue
        if data.get("config_hash") == cfg_hash and (metadata_path.parent / "metrics_final.json").exists():
            return metadata_path.parent
    return None


def _extract_run_dir(stdout: str) -> str | None:
    for line in reversed(stdout.splitlines()):
        marker = "Saved run to "
        if marker in line:
            return line.split(marker, 1)[1].strip()
    return None


def _visible_cuda_ids(gpu_ids: Any | None = None) -> list[str]:
    if gpu_ids:
        if isinstance(gpu_ids, list):
            return [str(item).strip() for item in gpu_ids if str(item).strip()]
        return [item.strip() for item in str(gpu_ids).split(",") if item.strip()]
    visible = os.environ.get("CUDA_VISIBLE_DEVICES")
    if visible:
        return [item.strip() for item in visible.split(",") if item.strip()]
    try:
        import torch

        return [str(index) for index in range(int(torch.cuda.device_count()))]
    except Exception:
        return []


def _default_parallel_jobs(requested_jobs: int | None, suite: dict[str, Any], gpu_ids: list[str]) -> int:
    if requested_jobs is not None:
        return max(1, int(requested_jobs))
    suite_jobs = suite.get("parallel_jobs")
    if suite_jobs is not None:
        return max(1, int(suite_jobs))
    return max(1, len(gpu_ids))


def _finish_process(
    row: dict[str, Any],
    process: subprocess.Popen[str],
    log_handle: Any,
    started: float,
    validate_artifacts: bool,
) -> bool:
    log_handle.close()
    row["returncode"] = process.returncode
    row["elapsed_seconds"] = time.monotonic() - started
    log_path = Path(row["log_path"])
    try:
        output = log_path.read_text(encoding="utf-8", errors="replace")
    except Exception:
        output = ""
    row["run_dir"] = _extract_run_dir(output)
    failed = False
    if process.returncode != 0:
        row["status"] = "training_failed"
        row["messages"].append(f"Training command failed with return code {process.returncode}")
        failed = True
    else:
        row["status"] = "completed"

    if row["status"] == "completed" and validate_artifacts and row.get("run_dir"):
        artifact_messages = validate_run_artifacts(row["run_dir"])
        row["artifact_validation"] = artifact_messages
        error_messages = [message for message in artifact_messages if message.startswith("ERROR:")]
        warning_messages = [message for message in artifact_messages if message.startswith("WARNING:")]
        if error_messages:
            row["status"] = "artifact_validation_failed"
            row["messages"].extend(error_messages)
            failed = True
        row["messages"].extend(warning_messages)
    return failed


def _timeout_process(row: dict[str, Any], process: subprocess.Popen[str], log_handle: Any, started: float) -> None:
    process.terminate()
    try:
        process.wait(timeout=30)
    except subprocess.TimeoutExpired:
        process.kill()
        process.wait(timeout=30)
    log_handle.close()
    row["status"] = "timeout"
    row["messages"].append(f"Timed out after {row.get('timeout_minutes')} minutes")
    row["returncode"] = None
    row["elapsed_seconds"] = time.monotonic() - started


def _run_pending_jobs(
    pending_jobs: list[dict[str, Any]],
    *,
    max_jobs: int,
    gpu_ids: list[str],
    timeout_minutes: float | None,
    validate_artifacts: bool,
    continue_on_error: bool,
) -> bool:
    failed = False
    active: list[dict[str, Any]] = []
    next_job = 0
    next_gpu = 0

    def launch(row: dict[str, Any]) -> None:
        nonlocal next_gpu
        log_path = Path(row["log_path"])
        log_path.parent.mkdir(parents=True, exist_ok=True)
        env = os.environ.copy()
        if gpu_ids:
            assigned_gpu = gpu_ids[next_gpu % len(gpu_ids)]
            next_gpu += 1
            env["CUDA_VISIBLE_DEVICES"] = assigned_gpu
            row["cuda_visible_devices"] = assigned_gpu
        command = row["command"]
        log = log_path.open("w", encoding="utf-8")
        log.write("$ " + " ".join(command) + "\n")
        if row.get("cuda_visible_devices") is not None:
            log.write(f"CUDA_VISIBLE_DEVICES={row['cuda_visible_devices']}\n")
        log.write("\n")
        log.flush()
        row["status"] = "running"
        print(f"Running {row['effective_config']} -> {log_path}", flush=True)
        process = subprocess.Popen(
            command,
            stdout=log,
            stderr=subprocess.STDOUT,
            text=True,
            env=env,
        )
        started = time.monotonic()
        active.append(
            {
                "row": row,
                "process": process,
                "log": log,
                "started": started,
                "deadline": started + timeout_minutes * 60 if timeout_minutes else None,
            }
        )

    while next_job < len(pending_jobs) or active:
        while next_job < len(pending_jobs) and len(active) < max_jobs and (continue_on_error or not failed):
            launch(pending_jobs[next_job])
            next_job += 1

        if not active:
            break

        for item in list(active):
            row = item["row"]
            process = item["process"]
            deadline = item["deadline"]
            if deadline is not None and time.monotonic() >= deadline and process.poll() is None:
                _timeout_process(row, process, item["log"], item["started"])
                active.remove(item)
                failed = True
                continue
            if process.poll() is not None:
                failed = _finish_process(
                    row,
                    process,
                    item["log"],
                    item["started"],
                    validate_artifacts,
                ) or failed
                active.remove(item)

        if active:
            time.sleep(1.0)

    if failed and not continue_on_error:
        for row in pending_jobs[next_job:]:
            row["status"] = "not_started_after_failure"
            row["messages"].append("Skipped because a previous run failed and stop-on-error is active")
    return failed


def _markdown_report(report: dict[str, Any]) -> str:
    lines = [
        "# Experiment Suite Report",
        "",
        f"- Suite: `{report['suite_name']}`",
        f"- Started: `{report['started_at']}`",
        f"- Finished: `{report['finished_at']}`",
        f"- Status: `{report['status']}`",
        f"- Total configs: `{len(report['runs'])}`",
        f"- Parallel jobs: `{report.get('parallel_jobs', 1)}`",
        f"- GPU IDs: `{', '.join(report.get('gpu_ids') or []) or '(none detected)'}`",
        "",
        "## Runs",
        "",
        "| Config | Seed | Status | GPU | Run Dir | Seconds | Log |",
        "| --- | ---: | --- | --- | --- | ---: | --- |",
    ]
    for row in report["runs"]:
        lines.append(
            f"| `{row['config']}` | `{row.get('seed') or ''}` | `{row['status']}` | "
            f"`{row.get('cuda_visible_devices') or ''}` | `{row.get('run_dir') or ''}` | "
            f"{row.get('elapsed_seconds') or 0:.1f} | `{row.get('log_path') or ''}` |"
        )
    lines.append("")
    failures = [row for row in report["runs"] if row["status"] not in {"completed", "skipped_existing", "dry_run_ok"}]
    if failures:
        lines.extend(["## Failures", ""])
        for row in failures:
            lines.append(f"### {row['config']}")
            lines.append("")
            for message in row.get("messages", []):
                lines.append(f"- {message}")
            if row.get("returncode") is not None:
                lines.append(f"- Return code: `{row['returncode']}`")
            lines.append("")
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Run many architecture/config experiments sequentially and safely.")
    parser.add_argument("configs", nargs="*", help="Training config YAML files. Overrides suite configs when provided.")
    parser.add_argument("--suite", default="configs/suites/experiment_suite.yaml")
    parser.add_argument("--results-dir", default="results")
    parser.add_argument("--logs-dir", default="reports/experiment_logs")
    parser.add_argument("--report-dir", default="reports/experiment_suites")
    parser.add_argument("--dry-run", action="store_true", help="Validate configs and print planned commands without training")
    parser.add_argument("--skip-existing", action="store_true", help="Skip configs whose config_hash already exists in results")
    parser.add_argument("--stop-on-error", action="store_true")
    parser.add_argument("--no-leaderboard", action="store_true")
    parser.add_argument("--timeout-minutes", type=float, default=None)
    parser.add_argument(
        "--jobs",
        type=int,
        default=None,
        help="Maximum concurrent training subprocesses. Defaults to one per visible CUDA GPU.",
    )
    parser.add_argument(
        "--gpu-ids",
        default=None,
        help="Comma-separated CUDA device IDs to assign to subprocesses, e.g. 0,1. Defaults to visible CUDA GPUs.",
    )
    args = parser.parse_args()

    suite = _load_suite(args.suite, args.configs)
    suite_name = _safe_name(str(suite.get("name", "experiment_suite")))
    continue_on_error = bool(suite.get("continue_on_error", True)) and not args.stop_on_error
    validate_artifacts = bool(suite.get("validate_artifacts", True))
    rebuild_leaderboard = bool(suite.get("rebuild_leaderboard", True)) and not args.no_leaderboard

    logs_dir = Path(args.logs_dir)
    logs_dir.mkdir(parents=True, exist_ok=True)
    report_dir = Path(args.report_dir)
    report_dir.mkdir(parents=True, exist_ok=True)
    results_dir = Path(args.results_dir)
    results_dir.mkdir(parents=True, exist_ok=True)
    generated_config_dir = report_dir / "generated_configs" / f"{utc_timestamp(compact=True)}_{suite_name}"
    gpu_ids = _visible_cuda_ids(args.gpu_ids or suite.get("gpu_ids"))
    parallel_jobs = _default_parallel_jobs(args.jobs, suite, gpu_ids)
    if not continue_on_error:
        parallel_jobs = 1

    started_at = utc_timestamp()
    run_rows: list[dict[str, Any]] = []
    pending_jobs: list[dict[str, Any]] = []
    failed = False

    seeds = _suite_seeds(suite)
    for config_path_text in suite["configs"]:
        config_path = Path(config_path_text)
        if not config_path.exists():
            for seed in seeds:
                row = {
                    "config": str(config_path),
                    "seed": seed,
                    "status": "config_missing",
                    "messages": [f"Config does not exist: {config_path}"],
                    "run_dir": None,
                    "log_path": None,
                    "elapsed_seconds": 0.0,
                }
                run_rows.append(row)
            failed = True
            if not continue_on_error:
                break
            continue

        base_config = load_yaml(config_path)
        for seed in seeds:
            config = _config_for_seed(base_config, seed)
            effective_config_path = config_path
            if seed is not None:
                generated_config_dir.mkdir(parents=True, exist_ok=True)
                effective_config_path = generated_config_dir / f"{config_path.stem}_seed{seed}.yaml"
                save_yaml(config, effective_config_path)
            row: dict[str, Any] = {
                "config": str(config_path),
                "effective_config": str(effective_config_path),
                "seed": seed,
                "status": "pending",
                "messages": [],
                "run_dir": None,
                "log_path": None,
                "elapsed_seconds": 0.0,
            }
            cfg_hash = config_fingerprint(config)
            row["config_hash"] = cfg_hash
            validation = validate_training_config(config, effective_config_path)
            row["messages"].extend(validation)
            if any(message.startswith("ERROR:") for message in validation):
                row["status"] = "validation_failed"
                run_rows.append(row)
                failed = True
                if not continue_on_error:
                    break
                continue

            existing = _find_existing_run(results_dir, cfg_hash) if args.skip_existing else None
            if existing:
                row["status"] = "skipped_existing"
                row["run_dir"] = str(existing)
                run_rows.append(row)
                continue

            command = [sys.executable, "scripts/train_model.py", "--config", str(effective_config_path)]
            row["command"] = command
            if args.dry_run:
                row["status"] = "dry_run_ok"
                run_rows.append(row)
                print("$ " + " ".join(command))
                continue

            seed_suffix = f"_seed{seed}" if seed is not None else ""
            log_path = logs_dir / f"{utc_timestamp(compact=True)}_{_safe_name(config_path.stem + seed_suffix)}.log"
            row["log_path"] = str(log_path)
            row["timeout_minutes"] = args.timeout_minutes
            run_rows.append(row)
            pending_jobs.append(row)
        if failed and not continue_on_error:
            break

    if pending_jobs and not args.dry_run:
        failed = _run_pending_jobs(
            pending_jobs,
            max_jobs=parallel_jobs,
            gpu_ids=gpu_ids,
            timeout_minutes=args.timeout_minutes,
            validate_artifacts=validate_artifacts,
            continue_on_error=continue_on_error,
        ) or failed

    leaderboard_returncode = None
    if rebuild_leaderboard and not args.dry_run:
        result = subprocess.run(
            [sys.executable, "scripts/compare_results.py", "--results-dir", str(results_dir)],
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
        )
        leaderboard_returncode = result.returncode
        (logs_dir / f"{utc_timestamp(compact=True)}_leaderboard.log").write_text(result.stdout, encoding="utf-8")
        if result.returncode != 0:
            failed = True

    report = {
        "suite_name": suite_name,
        "started_at": started_at,
        "finished_at": utc_timestamp(),
        "status": "failed" if failed else "ok",
        "dry_run": args.dry_run,
        "continue_on_error": continue_on_error,
        "seeds": seeds,
        "parallel_jobs": parallel_jobs,
        "gpu_ids": gpu_ids,
        "validate_artifacts": validate_artifacts,
        "rebuild_leaderboard": rebuild_leaderboard,
        "leaderboard_returncode": leaderboard_returncode,
        "runs": run_rows,
    }
    report_base = report_dir / f"{utc_timestamp(compact=True)}_{suite_name}"
    write_json(report, report_base.with_suffix(".json"))
    write_text(_markdown_report(report), report_base.with_suffix(".md"))
    print(f"Saved {report_base.with_suffix('.json')}")
    print(f"Saved {report_base.with_suffix('.md')}")
    if failed:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
