"""Scaffold one trainable idea folder per bt4_primitive_mixer mixer.

For every chess-research primitive that has a working bt4 mixer (a file under
``src/chess_nn_playground/models/architecture/bt4_mixers/`` registering a mixer
with that primitive's slug), this generates an ``a###_bt4_<slug>_mixer`` idea
folder: a BT4-style residual tower whose per-block spatial mixer is that
primitive.

Each primitive is GATED first: the script builds the full ``bt4_primitive_mixer``
model with that mixer and runs a forward + backward pass. Only primitives that
pass the gate get an idea folder. The model alias ``bt4_<slug>_mixer`` is
auto-registered by registry.py, so each idea's ``config.yaml model.name``
matches its slug, which the idea scaffold validator requires.

Run:
    PYTHONDONTWRITEBYTECODE=1 python scripts/ideas/scaffold_bt4_primitive_mixers.py
    PYTHONDONTWRITEBYTECODE=1 python scripts/ideas/scaffold_bt4_primitive_mixers.py --overwrite
"""

