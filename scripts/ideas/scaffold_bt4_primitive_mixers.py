REGISTRY_DIR = REPO_ROOT / "ideas" / "registry"
BASELINE_MIXERS = {"conv", "attention"}  # not chess-research primitives, skipped


def primitive_folders() -> list[Path]:
    folders = sorted(
        [
            p
            for p in REGISTRY_DIR.glob("p[0-9][0-9][0-9]_*")
            if p.is_dir()
        ]
        + [
            p
            for p in REGISTRY_DIR.glob("i24[4-8]_*")
            if p.is_dir()
        ]
    )
    return folders


def slug_of(folder: Path) -> str:
    # strip the "pNNN_" / "iNNN_" prefix
    return folder.name.split("_", 1)[1]


def gate_mixer(mixer: str) -> tuple[bool, str]:
    """Build the full bt4_primitive_mixer with this mixer; forward + backward."""
    try:
        model = build_model(
            "bt4_primitive_mixer",
            {"input_channels": 18, "num_classes": 1, "channels": 64, "num_blocks": 4, "mixer": mixer},
        )
        x = torch.randn(2, 18, 8, 8)
        out = model(x)["logits"]
        if out.shape != (2,) or not torch.isfinite(out).all():
            return False, f"bad forward output shape/finite: {tuple(out.shape)}"
        out.sum().backward()
        return True, "ok"
    except Exception as exc:  # noqa: BLE001
        return False, f"{type(exc).__name__}: {exc}"


