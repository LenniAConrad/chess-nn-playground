def architecture_md(slug: str, primitive_folder: str) -> str:
    return f"""# Architecture

`BT4 Primitive Mixer ({slug})` is a controlled architecture study, not a new
primitive. It holds the BT4-style residual tower fixed and swaps only the
per-block spatial-mixing operator.

## Implementation Binding

- Registered model name: `bt4_{slug}_mixer` (alias of `bt4_primitive_mixer`
  with `mixer={slug}`)
- Tower: `src/chess_nn_playground/models/architecture/bt4_primitive_mixer.py`
- Mixer: `src/chess_nn_playground/models/architecture/bt4_mixers/{slug}.py`
- Source primitive idea: `{primitive_folder}`

## What this is

The `bt4_primitive_mixer` tower mirrors `lc0_bt4_classifier`: a stem conv, N
residual blocks each `mixer -> SqueezeExcite -> +residual -> ReLU`, then the
value head. The original lc0_bt4 block mixes spatially with a pair of 3x3
convs. Here that mixer is replaced by the `{slug}` primitive, adapted to the
shape-preserving `(B, C, 8, 8) -> (B, C, 8, 8)` mixer contract.

Because the tower, optimizer protocol, data contract, and training
hyperparameters are identical across every `a###_bt4_*_mixer` idea and the
`conv` / `attention` baselines, the only variable is the mixer. That makes
this the cleanest available test of "is the `{slug}` primitive a better
spatial mixer than conv or attention?".

## Contract

- Input: `(B, 18, 8, 8)` simple_18 board tensor only.
- Output: `dict` with `logits` of shape `(B,)` for the one-logit puzzle_binary
  BCE-with-logits trainer.
"""


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--overwrite", action="store_true", help="recreate folders that already exist")
    ap.add_argument("--manifest", type=Path, default=REPO_ROOT / "reports" / "bt4_primitive_mixers" / "manifest.txt")
    args = ap.parse_args()

    registered = set(available_mixers())
    folders = primitive_folders()
    created: list[str] = []
    gated_out: list[tuple[str, str]] = []
    validation_failed: list[tuple[str, list[str]]] = []
    skipped_existing: list[str] = []

    next_id = 1
    for folder in folders:
        slug = slug_of(folder)
        if slug not in registered:
            gated_out.append((slug, "no registered mixer"))
            continue
        ok, msg = gate_mixer(slug)
        if not ok:
            gated_out.append((slug, msg))
            continue

        idea_id = f"a{next_id:03d}"
        next_id += 1
        idea_folder = REGISTRY_DIR / f"{idea_id}_bt4_{slug}_mixer"

        if idea_folder.exists() and not args.overwrite:
            skipped_existing.append(idea_folder.name)
            continue

        (idea_folder / "runs").mkdir(parents=True, exist_ok=True)
        (idea_folder / "idea.yaml").write_text(idea_yaml(idea_id, slug, folder.name))
        (idea_folder / "config.yaml").write_text(config_yaml(idea_id, slug))
        (idea_folder / "model.py").write_text(model_py(slug))
        (idea_folder / "train.py").write_text(TRAIN_PY)
        (idea_folder / "architecture.md").write_text(architecture_md(slug, folder.name))

        result = validate_idea_for_training(idea_folder)
        if not result["valid"]:
            validation_failed.append((idea_folder.name, result["issues"]))
        else:
            created.append(idea_folder.name)

    args.manifest.parent.mkdir(parents=True, exist_ok=True)
    args.manifest.write_text("\n".join(sorted(created)) + ("\n" if created else ""))

    print(f"Created + validated: {len(created)}")
    for name in sorted(created):
        print(f"  OK   {name}")
    if skipped_existing:
        print(f"Skipped (already exist, use --overwrite): {len(skipped_existing)}")
        for name in skipped_existing:
            print(f"  SKIP {name}")
    if gated_out:
        print(f"Gated out (mixer build/forward failed): {len(gated_out)}")
        for slug, why in gated_out:
            print(f"  GATE {slug}: {why}")
    if validation_failed:
        print(f"Validation failed: {len(validation_failed)}")
        for name, issues in validation_failed:
            print(f"  FAIL {name}")
            for issue in issues:
                print(f"        {issue}")
    print(f"\nManifest of trainable ideas: {args.manifest}")


if __name__ == "__main__":
    main()
